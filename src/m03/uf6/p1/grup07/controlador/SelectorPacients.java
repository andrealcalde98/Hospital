    private void PacientsActionPerformed(java.awt.event.ActionEvent evt) {                                          
        if(Pacients.getSelectedIndex() < 0){
            // Nada que hacer
        }else{
            String pacients = (String)(Pacients.getSelectedItem());
            try{
                ResultSet resultat = consultaBBDD(
                        //"SELECT jugadores.nombre, jugadores.dorsal, jugadores.edad"
                        "SELECT * FROM PACIENTS "
                                + "WHERE pacients.NIF LIKE '" + pacients + "'");
                TableModel model = new ModeloDeTablaSimple(resultat);
                actualitzaTaula(tabPacients, model);
                // Afegim un listener per salvar els canvis a la BBDD en modificar
                // el valor d'un camp de la taula
                tabPacients.getModel().addTableModelListener(new TableModelListener() {
                    @Override
                    public void tableChanged(TableModelEvent e) {
                        actualitzaTaulaPacients(e);
                    }
                });
                
            }catch(SQLException e){
                e.printStackTrace();
            }
        }
    }                                         

    private void actualitzaTaulaPacients(TableModelEvent e){
        int fila = e.getFirstRow();
        PreparedStatement sentencia = null;
        Connection con = null;
        TableModel dades = tabPacients.getModel();
        try {
            con = GestorConnexio.getConnection();
            String consulta = "UPDATE pacients SET nom = ?, cognom1 = ?, "
              + "cognom2 = ?, numSegSocial = ?, NIF = ?, telefon = ? WHERE NIF = ?";
            sentencia = con.prepareStatement(consulta);
            sentencia.setString(1, (String)(dades.getValueAt(fila, 0)));
            sentencia.setString(2, (String)(dades.getValueAt(fila, 1)));
            sentencia.setString(3, (String)(dades.getValueAt(fila, 2)));
            sentencia.setString(4, (String)(dades.getValueAt(fila, 3)));
            sentencia.setString(5, (String)(dades.getValueAt(fila, 4)));
            sentencia.setInt(6, Integer.parseInt(dades.getValueAt(fila, 5).toString()));
            sentencia.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(SelectorMetges.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
